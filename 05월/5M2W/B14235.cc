int N, Q, X;
priority_queue<int> pq;

int main() {
	ios::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);

	cin >> N;
	for (int i = 0; i < N; i++) {
		cin >> Q;
		if (!Q) {
			if (pq.empty()) cout << -1 << '\n';
			else {
				cout << pq.top() << '\n';
				pq.pop();
			}
		}
		else {
			for (int j = 0; j < Q; j++) {
				cin >> X;
				pq.push(X);
			}
		}
	}
	return 0;
}